function goingForward(){
    console.log(1)
    if(current-1 <= 0){
        current += 1
    }
    clinicTitleName.innerText = clinic[current].name
    clinicAddress.innerText = clinic[current].address
    clinicPhone.innerText = clinic[current].phone
    clinicEmail.innerText = clinic[current].email
}

